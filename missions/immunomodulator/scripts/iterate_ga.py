"""
GA Generation Loop — Automated A3→A6 pipeline

Implements the channel-delta convergence loop from docs/ga_generation/05_ALGORITHM.md:
  A3: Ask Gemini for ideal channel spec per claim
  A4: Compare ideal channels vs current parametric SVG → compute delta
  A5: Generate layout fixes to close top-N delta channels
  A6: Apply fixes to YAML, compile, validate via reader sim

Usage:
  python iterate_ga.py [--version 18] [--max-iters 5] [--target 0.50]
"""

